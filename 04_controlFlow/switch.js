const fruit = "Apple";

switch (fruit) {
  case "Apple":
    console.log("It's an apple");
    break;
  case "Banana":
    console.log("It's a banana");
    break;
  case "Orange":
    console.log("It's an orange");
    break;
  default:
    console.log("Unknown fruit");
}
